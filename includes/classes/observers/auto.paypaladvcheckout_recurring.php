    protected function orderUsesPaypalacFamilyModule(int $ordersId): bool
    {
        if ($ordersId <= 0) {
            return false;
        }

        global $db;

        $result = $db->Execute(
            "SELECT payment_module_code"
            . " FROM " . TABLE_ORDERS
            . " WHERE orders_id = " . (int)$ordersId
            . " LIMIT 1"
        );

        if (!is_object($result) || $result->EOF) {
            return false;
        }

        $module = strtolower((string)$result->fields['payment_module_code']);
        return $module === 'paypalac' || strpos($module, 'paypalac_') === 0;
    }

    protected function normalizeAttributeKey(string $label): string
    {
        $label = strtolower($label);
        $label = preg_replace('/[^a-z0-9]+/', '_', $label) ?? $label;
        return trim($label, '_');
    }

    /**
     * @param array<string,string> $attributeMap
     */
    protected function attributeMapHasAcceptedAutomaticRenewal(array $attributeMap): bool
    {
        if (function_exists('paypalac_attribute_map_has_accepted_automatic_renewal')) {
            return paypalac_attribute_map_has_accepted_automatic_renewal($attributeMap);
        }

        foreach ($attributeMap as $key => $value) {
            $normalizedKey = preg_replace('/[^a-z0-9]/', '', strtolower((string) $key)) ?? '';
            if ($normalizedKey !== 'acceptautomaticrenewal' && $normalizedKey !== 'automaticrenewal') {
                continue;
            }

            $v = strtolower(trim((string) $value));
            if ($v !== '' && !in_array($v, ['0', 'false', 'off', 'no', 'unchecked'], true)) {
                return true;
            }
        }

        return false;
    }

    /**
     * Get saved_credit_card_id from vault record by looking up in TABLE_SAVED_CREDIT_CARDS.
     * 
     * @param array|null $vaultRecord The vault record from VaultManager
     * @return int The saved_credit_card_id or 0 if not found
     */
    protected function getSavedCreditCardId(?array $vaultRecord): int
    {
        if ($vaultRecord === null) {
            return 0;
        }
        
        $vaultId = (string)($vaultRecord['vault_id'] ?? '');
        if ($vaultId === '') {
            return 0;
        }
        
        global $db;
        
        // Ensure saved_credit_cards table exists and has all required columns
        // This must be called unconditionally because the table constants are always defined
        // in extra_datafiles/ppac_database_tables.php, so conditional checks won't work
        if (class_exists('PayPalAdvancedCheckout\\Common\\SavedCreditCardsManager')) {
            \PayPalAdvancedCheckout\Common\SavedCreditCardsManager::ensureSchema();
        }
        
        if (!defined('TABLE_SAVED_CREDIT_CARDS')) {
            $this->log->write("    ERROR: TABLE_SAVED_CREDIT_CARDS constant not defined.");
            return 0;
        }
        
        // Use parameterized query approach - zen_db_input provides escaping
        $safeVaultId = zen_db_input($vaultId);
        $result = $db->Execute(
            "SELECT saved_credit_card_id FROM " . TABLE_SAVED_CREDIT_CARDS . "
             WHERE vault_id = '$safeVaultId'
             AND is_deleted = 0
             LIMIT 1"
        );
        
        if ($result->EOF) {
            $this->log->write("    No saved_credit_card found for vault_id: $vaultId");
            return 0;
        }
        
        return (int)$result->fields['saved_credit_card_id'];
    }

    /**
     * Calculate the next billing date based on subscription attributes.
     * 
     * @param array $subscriptionAttributes The subscription attributes
     * @return string The next billing date in Y-m-d format
     */
    protected function calculateNextBillingDate(array $subscriptionAttributes): string
    {
        // Check for a future start date in the product's order attributes.
        // If the subscription has a start date that is in the future, use it as the
        // next billing date instead of calculating from today + billing period.
        $attributeMap = $subscriptionAttributes['attributes'] ?? [];
        $startDate = $attributeMap['start_date'] ?? ($attributeMap['startdate'] ?? '');
        if ($startDate !== '') {
            $startTimestamp = strtotime($startDate);
            if ($startTimestamp !== false && $startTimestamp > strtotime('today')) {
                return date('Y-m-d', $startTimestamp);
            }
        }

        $billingPeriod = $subscriptionAttributes['billing_period'] ?? 'MONTH';
        $billingFrequency = (int)($subscriptionAttributes['billing_frequency'] ?? 1);
        
        if ($billingFrequency <= 0) {
            $billingFrequency = 1;
        }
        
        // Start from today
        $date = new DateTime();
        
        // Map PayPal period to PHP DateInterval
        switch (strtoupper($billingPeriod)) {
            case 'DAY':
                $date->modify('+' . $billingFrequency . ' days');
                break;
            case 'WEEK':
                $date->modify('+' . ($billingFrequency * 7) . ' days');
                break;
            case 'SEMI_MONTH':
                // Semi-monthly is approximately 15 days
                $date->modify('+15 days');
                break;
            case 'YEAR':
                $date->modify('+' . $billingFrequency . ' years');
                break;
            case 'MONTH':
            default:
                $date->modify('+' . $billingFrequency . ' months');
                break;
        }
        
        return $date->format('Y-m-d');
    }

    /**
     * Sync vault record to saved_credit_cards table for Zen Cart-managed subscriptions.
     * 
     * This method creates a record in TABLE_SAVED_CREDIT_CARDS based on the vault record,
     * enabling Zen Cart-managed subscriptions to reference the saved card via saved_credit_card_id.
     * If a record with the same vault_id already exists, no action is taken.
     * 
     * @param array $vaultRecord The vault record from VaultManager
     */
    protected function syncVaultToSavedCreditCards(array $vaultRecord): void
    {
        global $db;
        
        // Ensure saved_credit_cards table exists and has all required columns
        // This must be called unconditionally because the table constants are always defined
        // in extra_datafiles/ppac_database_tables.php, so conditional checks won't work
        if (class_exists('PayPalAdvancedCheckout\\Common\\SavedCreditCardsManager')) {
            \PayPalAdvancedCheckout\Common\SavedCreditCardsManager::ensureSchema();
        }
        
        if (!defined('TABLE_SAVED_CREDIT_CARDS')) {
            $this->log->write("    ERROR: TABLE_SAVED_CREDIT_CARDS constant not defined, cannot sync vault to saved cards.");
            return;
        }
        
        $vaultId = (string)($vaultRecord['vault_id'] ?? '');
        if ($vaultId === '') {
            $this->log->write("    WARNING: Cannot sync vault to saved cards - vault_id is empty.");
            return;
        }

        if ((int)($vaultRecord['visible'] ?? 0) !== 1) {
            $this->log->write("    Skipping saved_credit_cards sync for backend-only vault token: $vaultId");
            return;
        }
        
        // Check if an active (non-deleted) record already exists for this vault_id.
        // The is_deleted=0 filter must match getSavedCreditCardId() so that both methods
        // agree on whether a usable record exists. Without this, a soft-deleted record
        // would cause syncVault to skip creation while getSavedCreditCardId returns 0.
        $safeVaultId = zen_db_input($vaultId);
        $existing = $db->Execute(
            "SELECT saved_credit_card_id, is_deleted FROM " . TABLE_SAVED_CREDIT_CARDS . "
             WHERE vault_id = '$safeVaultId'
             LIMIT 1"
        );
        
        if (!$existing->EOF) {
            $existingId = (int)$existing->fields['saved_credit_card_id'];
            $isDeleted = (int)($existing->fields['is_deleted'] ?? 0);
            
            if ($isDeleted === 0) {
                $this->log->write("    Saved credit card record already exists for vault_id: $vaultId (ID: $existingId)");
                return;
            }
            
            // Record exists but is soft-deleted. Reactivate it so getSavedCreditCardId can find it.
            $this->log->write("    Reactivating soft-deleted saved credit card #$existingId for vault_id: $vaultId");
            $now = date('Y-m-d H:i:s');
            $db->Execute(
                "UPDATE " . TABLE_SAVED_CREDIT_CARDS . "
                 SET is_deleted = 0, last_modified = '$now'
                 WHERE saved_credit_card_id = $existingId"
            );
            return;
        }
        
        // Extract card data from vault record
        $customersId = (int)($vaultRecord['customers_id'] ?? 0);
        
        if ($customersId <= 0) {
            $this->log->write("    WARNING: Cannot sync vault to saved cards - customers_id is invalid.");
            return;
        }
        
        $cardType = (string)($vaultRecord['brand'] ?? $vaultRecord['card_type'] ?? '');
        $lastDigits = (string)($vaultRecord['last_digits'] ?? '');
        $expiry = (string)($vaultRecord['expiry'] ?? ''); // Format: YYYY-MM
        $holderName = (string)($vaultRecord['cardholder_name'] ?? '');
        
        // Parse expiry into month and year
        $expiryMonth = '';
        $expiryYear = '';
        if ($expiry !== '' && preg_match('/^(\d{4})-(\d{2})$/', $expiry, $matches)) {
            $expiryYear = $matches[1];
            $expiryMonth = $matches[2];
        }
        
        // Build the INSERT dynamically to include legacy columns if they exist.
        // The legacy saved_credit_cards table (created by installer 1_0_0.php) has
        // NOT NULL columns without DEFAULT values (name_on_card, paypal_transaction_id,
        // is_primary). Omitting these from the INSERT causes a failure in MySQL strict mode.
        // ensureSchema() sets DEFAULT values on these columns, but we also include them
        // explicitly as a safety net in case ensureSchema hasn't run yet for this request.
        $now = date('Y-m-d H:i:s');
        $safeCustomersId = (int)$customersId;
        $safeType = zen_db_input($cardType);
        $safeLastDigits = zen_db_input($lastDigits);
        $safeExpiryMonth = zen_db_input($expiryMonth);
        $safeExpiryYear = zen_db_input($expiryYear);
        $safeHolderName = zen_db_input($holderName);
        
        $columns = 'customers_id, type, last_digits, expiry_month, expiry_year, holder_name, '
                 . 'billing_address_id, is_default, is_deleted, vault_id, date_added, last_modified';
        $values = "$safeCustomersId, '$safeType', '$safeLastDigits', '$safeExpiryMonth', '$safeExpiryYear', "
                . "'$safeHolderName', 0, 0, 0, '$safeVaultId', '$now', '$now'";
        
        // Include legacy columns if they exist in the table to avoid strict mode errors
        $legacyColumns = [
            'name_on_card' => "'" . zen_db_input($holderName) . "'",
            'paypal_transaction_id' => "''",
            'is_primary' => "0",
        ];
        foreach ($legacyColumns as $col => $val) {
            $check = $db->Execute(
                "SHOW COLUMNS FROM " . TABLE_SAVED_CREDIT_CARDS . " LIKE '$col'"
            );
            if ($check instanceof \queryFactoryResult && $check->RecordCount() > 0) {
                $columns .= ", $col";
                $values .= ", $val";
            }
        }
        
        $db->Execute(
            "INSERT INTO " . TABLE_SAVED_CREDIT_CARDS . " ($columns) VALUES ($values)"
        );
        
        $savedCreditCardId = $db->Insert_ID();
        $this->log->write("    SUCCESS: Created saved_credit_card record #$savedCreditCardId for vault_id: $vaultId");
    }

    /**
     * True when PayPal Advanced Checkout is configured for authorize (not final capture) on checkout.
     */
    protected function paypalacCheckoutUsesAuthorizeIntent(): bool
    {
        return defined('MODULE_PAYMENT_PAYPALAC_TRANSACTION_MODE')
            && MODULE_PAYMENT_PAYPALAC_TRANSACTION_MODE !== 'Final Sale';
    }
